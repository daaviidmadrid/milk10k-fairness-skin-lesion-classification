"""PyTorch datasets for supervised and multimodal MILK10k experiments.

The classes here read the CSV files generated by `build_milk10k_dataset.py`.
They keep labels and sensitive attributes available for metrics and losses.
"""

from __future__ import annotations

from pathlib import Path
from typing import Callable

import pandas as pd
import torch
from PIL import Image
from torch.utils.data import Dataset

from milk10k.data.labels import make_binary_label


# Convert task labels into stable integer class indices.
def build_class_mapping(df: pd.DataFrame, task: str) -> dict[str, int]:
    """Build class-to-index mapping for 11f or 2f tasks."""
    if task == "2f":
        names = ["benign", "malignant"]
    else:
        names = sorted(df["class_name"].dropna().unique().tolist())
    return {name: idx for idx, name in enumerate(names)}


# Return the class names ordered by their integer id.
def class_names_from_mapping(mapping: dict[str, int]) -> list[str]:
    """Return class names ordered by index."""
    return [name for name, _ in sorted(mapping.items(), key=lambda kv: kv[1])]


# Convert one dataframe row to the class name used by the selected task.
def task_label_name(row: pd.Series, task: str) -> str:
    """Return the 11-class or binary label name for one row."""
    return make_binary_label(row["class_name"]) if task == "2f" else row["class_name"]


class ImageCsvDataset(Dataset):
    """Supervised image dataset backed by a generated MILK10k split CSV."""

    def __init__(
        self,
        csv_path: str | Path,
        task: str,
        transform: Callable | None = None,
        class_to_idx: dict[str, int] | None = None,
        rows: pd.DataFrame | None = None,
    ) -> None:
        self.csv_path = Path(csv_path)
        self.df = rows.copy() if rows is not None else pd.read_csv(self.csv_path)
        self.task = task
        self.transform = transform
        self.class_to_idx = class_to_idx or build_class_mapping(self.df, task)

    def __len__(self) -> int:
        """Return the number of rows in the split."""
        return len(self.df)

    def _label_name(self, row: pd.Series) -> str:
        """Return the task-specific class name for one row."""
        return task_label_name(row, self.task)

    def __getitem__(self, index: int) -> dict[str, torch.Tensor | int | str]:
        """Load one image and return tensors plus metadata used by metrics."""
        row = self.df.iloc[index]
        image = Image.open(row["img_path"]).convert("RGB")
        if self.transform is not None:
            image = self.transform(image)
        label_name = self._label_name(row)
        return {
            "image": image,
            "label": torch.tensor(self.class_to_idx[label_name], dtype=torch.long),
            "skin_tone": torch.tensor(int(row.get("skin_tone_class", 0)), dtype=torch.long),
            "sex": str(row.get("sex", "unknown")),
            "age": torch.tensor(float(row.get("age_approx", -1) if pd.notna(row.get("age_approx", -1)) else -1)),
            "class_name": label_name,
            "path": str(row["img_path"]),
        }


class RepeatedAugmentationDataset(Dataset):
    """Virtual dataset that repeats minority-class samples with augmented views."""

    def __init__(
        self,
        base_df: pd.DataFrame,
        task: str,
        base_transform: Callable,
        aug_transform: Callable,
        class_to_idx: dict[str, int],
        exponent: float = 0.5,
        cap: int = 6,
    ) -> None:
        self.base_transform = base_transform
        self.aug_transform = aug_transform
        self.dataset = ImageCsvDataset("", task, transform=None, class_to_idx=class_to_idx, rows=base_df)
        label_names = base_df.apply(lambda row: task_label_name(row, task), axis=1)
        counts = label_names.value_counts()
        max_count = counts.max()
        repeats = {
            cls: max(1, min(cap, int(round((max_count / count) ** exponent))))
            for cls, count in counts.items()
        }
        expanded: list[tuple[int, bool]] = []
        sampling_class_names: list[str] = []
        for row_idx, row in base_df.reset_index(drop=True).iterrows():
            label_name = task_label_name(row, task)
            repeat = repeats[label_name]
            expanded.append((row_idx, False))
            sampling_class_names.append(label_name)
            expanded.extend((row_idx, True) for _ in range(max(0, repeat - 1)))
            sampling_class_names.extend([label_name] * max(0, repeat - 1))
        self.expanded = expanded
        self.sampling_class_names = sampling_class_names
        self.sampling_class_counts = counts.to_dict()

    def __len__(self) -> int:
        """Return the virtual dataset size after class-aware repetition."""
        return len(self.expanded)

    def __getitem__(self, index: int) -> dict[str, torch.Tensor | int | str]:
        """Load the original or augmented view for one virtual sample."""
        row_idx, augmented = self.expanded[index]
        item = self.dataset[row_idx]
        image = Image.open(item["path"]).convert("RGB")
        item["image"] = self.aug_transform(image) if augmented else self.base_transform(image)
        return item


class PairedModalDataset(Dataset):
    """Clinical-dermoscopic paired dataset for multimodal SSL pretraining."""

    def __init__(self, clinical_csv: str | Path, dermoscopic_csv: str | Path, transform: Callable) -> None:
        clinical = pd.read_csv(clinical_csv)
        derm = pd.read_csv(dermoscopic_csv)
        self.df = clinical[["lesion_id", "img_path"]].merge(
            derm[["lesion_id", "img_path"]],
            on="lesion_id",
            suffixes=("_clinical", "_dermoscopic"),
        )
        self.transform = transform

    def __len__(self) -> int:
        """Return the number of paired lesions."""
        return len(self.df)

    def __getitem__(self, index: int) -> dict[str, torch.Tensor | str]:
        """Return the paired clinical and dermoscopic views for one lesion."""
        row = self.df.iloc[index]
        clinical = Image.open(row["img_path_clinical"]).convert("RGB")
        dermoscopic = Image.open(row["img_path_dermoscopic"]).convert("RGB")
        return {
            "clinical": self.transform(clinical),
            "dermoscopic": self.transform(dermoscopic),
            "lesion_id": str(row["lesion_id"]),
        }
